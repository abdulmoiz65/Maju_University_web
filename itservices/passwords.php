<h4>Guidelines for Choosing a Password</h4> <br>
<h6>Passwords must be at least 10 characters</h6> <br>

<ul class="list-group">
  
  
  <li class="list-group-item list-group-item-warning">Passwords must contain upper, lowercase, numbers and symbols. i.e. Abc123!@#a </li>
  <li class="list-group-item list-group-item-info">Do not use blank spaces.</li>
  <li class="list-group-item list-group-item-danger">Passwords should be something you can easily remember, yet hard for someone else to guess. For example, a phrase like "too hot today for me" could be 2Ht2dy4me!</li>
  <li class="list-group-item list-group-item-warning">Passwords cannot be all numbers, and cannot contain alphabetic or numerical sequences (i.e., "cde" or "345").</li>
  <li class="list-group-item list-group-item-info">Passwords cannot be your name or your personal information provided to university. Do not use your initials preceded or followed by numbers.</li>
</ul>
<br><br>


