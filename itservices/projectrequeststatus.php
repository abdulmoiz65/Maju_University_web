  <h3>Project Request & Status</h3>
  <ul class="list-group">

    
    <li class="list-group-item list-group-item-secondary">Projects Requests are used to request development such as enhancements to administrative systems, major or upgrades, or data services such as third-party integrations. </li>

  </ul>
  <br><br>

  
  <h3>Services provided by the Applications Team</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-primary"><strong>REVIEW </strong> : All new requests will be reviewed and assessed for general size, feasibility, and priority, but will remain the project request backlog until departmental resources are available to work on it. A project request may be withdrawn or converted to a service request if the project is not feasible or if it is determined to be small in scope.</li>
    <li class="list-group-item list-group-item-secondary"><strong>ANALYSIS</strong> : Once departmental resources are available, the request becomes an Active Project with a status of Analysis. During this phase, any specific details (aka business requirements) is gathered. A project timeline and project plan may be devised, depending on the type of project and what research and testing is required.</li>
    <li class="list-group-item list-group-item-success"><strong>IN PROCESS</strong> : When appropriate, a project plan is reviewed by the requestor. Once approved, by the project team the project status will be updated to In Process. The assigned staff will work with the requestor to complete the project in accordance with any project plan. </li>
    <li class="list-group-item list-group-item-danger"><strong>USER TESTING</strong> : The final project phase is User Testing. In general projects are moved to the User Testing phase once primary development is completed and the project is waiting for final user acceptance testing before being moved to production. <br> <strong>Note:</strong> some large project may require an iterative project plan with multiple phases of development and user acceptance testing.</li>

  </ul>
