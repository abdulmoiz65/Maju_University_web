  <h1>Rules of Conduct</h1> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> Students using the Computer LABs are required to comply with the following rules of conduct. Lab Assistant have responsibility for enforcing these rules, and will notify LAB users if they are in violation. Failure to comply with these rules may result in removal from the LABs, termination of access, loss of computing privileges, disciplinary review, expulsion, legal action, termination of employment, or other disciplinary action.</li>
    
  </ul>
  <br><br>

  <h3>Noise</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> Computer Labs serve as a shared study area, where noise must be kept to a minimum.  In consideration of other students working in the LAB, you are expected to speak softly and limited conversation with others. You may not play music in the LAB.  Persons making excessive noise and disturbing others will be asked to leave the LAB. Repeated offenders will have their accounts revoked or restricted.</li>
    
  </ul>
  <br><br>
  
  <h3>Food & Drink</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> No food or drinks are allowed in the computer LABs. drinks or any type of food should be left outside the labs or left on the tables at the LAB entrances.</li>
    
  </ul>
  <br><br>

  <h3>Smoking</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> University campuses, including the computer LABs is a smoke-free zone. There will be no smoking, chewing tobacco or other tobacco products allowed in the LABs at any time.</li>
    
  </ul>
  <br><br>

  <h3>Children in the Labs</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> Children are not allowed in the computer LAB, and prohibited from using any University computer equipment or systems.  University reserves the right to request that children be removed from the LAB.</li>
    
  </ul>
  <br><br>

  <h3>Storing Files and Data</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> Saving data files and using personal storage on any LAB computer are not allowed. You may save personal files on the cloud storage.</li>
    
  </ul>
  <br><br>

  <h3>Logging In</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> Students are required to log into computer LAB machines and systems with their username and password. You may not use someone else's login and password to access university computer systems. You may not allow anyone to use your login and password to university computer systems. Violation of these rules can result in serious disciplinary action.</li>
    
  </ul>
  <br><br>

  <h3>Logging Out</h3> 
  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary">You must log out of the system when you leave the computer LABs, even if only for a short time.  Failure to do so may result, if someone else accessing your account without your knowledge. Computers may not be saved for your use while you are away from the LAB.  You are responsible for any violation of university policies that occur with your account, even when you did not perform the action.</li>
    
  </ul>
