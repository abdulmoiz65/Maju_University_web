  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary"> We all know that social networking is the way to connect with your friends. But have you considered what information you share online? Others have! Security experts agree that your privacy is at risk if you include personal information about yourself, such as full name, address, phone number, or account numbers. It's also important to consider what information and photos are posted by you or others who know you.</li>
    
