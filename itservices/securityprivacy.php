  <ul class="list-group">

    <li class="list-group-item list-group-item-secondary">Windows Defender is free and supported on Windows 10 and Windows 11 respectively, earlier versions of Windows are end-of-life and no longer supported.  Additionally, security patches are no longer available from Microsoft and thus these older versions of Windows (XP/Vista,7,8.x) are highly discouraged from being utilized on the university blocks due to serious security concerns. University owned/issued computers are already having an windows defender installed. </li>
    

